var1 = "hello"
var2 = "world"
var3 = "!"

# if statements
if var1 == var2:
	print("var1 is equal to var2")
elif var1 > var2:
	print("var1 is greater than var2")
else:
	print("var1 is less than var2")

# if and else statements
if (var1 == var2) | (var1 == var3):
	print("var1 is equal to var2 and var3")
elif var1 > var2:
	print("var1 is greater than var2")
else:
	print("var1 is less than var2")


